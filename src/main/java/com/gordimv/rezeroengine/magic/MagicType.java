package com.gordimv.rezeroengine.magic;

/**
 * Represents the type of magic a player possesses.
 *
 * NORMAL:
 * Standard elemental magic.
 *
 * SPIRIT_ARTS:
 * Spirit contract magic.
 *
 * BOTH:
 * Capable of both.
 *
 * NONE:
 * Cannot currently use magic.
 */
public enum MagicType {

    NONE,

    NORMAL,

    SPIRIT_ARTS,

    BOTH
}