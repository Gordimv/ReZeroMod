package com.gordimv.rezeroengine.character;

/**
 * ============================================================
 * Project Lugunica
 *
 * PotentialType
 *
 * Represents every innate aptitude that can be generated
 * during character creation.
 *
 * Systems should iterate over PotentialType instead of
 * hardcoding individual aptitudes.
 * ============================================================
 */
public enum PotentialType {

    MAGICAL,

    PHYSICAL,

    MENTAL,

    CRAFTING,

    SOCIAL

}